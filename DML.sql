--2. Print names of faculties, where financing fund of departments 
--exceeds financing fund of the faculty.

select Faculties.[Name] from Departments,Faculties
where Departments.FacultyId = Faculties.Id And Departments.Financing > Faculties.Financing

--============================================================================================================================

--3. Print names of the group curators and groups names they are supervising.

select Groups.[Name] as 'Group_Name', Curators.[Name] as 'Curator_Name' from Groups,Curators,GroupsCurators
where Groups.Id=GroupsCurators.GroupId and Curators.Id=GroupsCurators.CuratorId

--============================================================================================================================

--4. Print names of the teachers who deliver lectures in the group "P107".
select Teachers.[Name] from Teachers,Lectures,Groups,GroupsLectures
where Groups.Id=GroupsLectures.GroupId And Lectures.Id=GroupsLectures.LectureId 
And Teachers.Id=Lectures.TeacherId And Groups.[Name] = 'P107'

