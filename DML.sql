--5. Print names of the teachers and names of the faculties where they are lecturing.

select distinct Teachers.[Name] as TeacherName,Faculties.[Name] as FacultyName
from  Teachers, Lectures, Subjects, GroupsLectures, Groups, Departments, Faculties
where Lectures.TeacherId = Teachers.Id AND Lectures.Id = GroupsLectures.LectureId AND GroupsLectures.GroupId = Groups.Id
AND Groups.DepartmentId = Departments.Id AND Departments.FacultyId = Faculties.Id;


